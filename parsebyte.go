func ToByte(input []byte) byte {
  var lookup [3]byte
  var input_length = len(input)
  if input_length > 3 {
    panic("Input out of bounds")
  }
  for i:=0; i<input_length; i++ {
    lookup[i] = input[i]
  }
  result, ok := _parsebyte_fwd_lookup_table[lookup]
  if ok {
    return(result)
  } else {
    panic("Input invalid")
  }
}

